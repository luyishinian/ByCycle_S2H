package com.wjf.bean;

public class Cycle {
	private int id;
	private String c_name;
	private String c_img;
	private String c_message;
	private String c_address;
	private int c_count;
	private int c_collect;
	private float c_price;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_img() {
		return c_img;
	}
	public void setC_img(String c_img) {
		this.c_img = c_img;
	}
	public String getC_message() {
		return c_message;
	}
	public void setC_message(String c_message) {
		this.c_message = c_message;
	}
	public String getC_address() {
		return c_address;
	}
	public void setC_address(String c_address) {
		this.c_address = c_address;
	}
	public int getC_count() {
		return c_count;
	}
	public void setC_count(int c_count) {
		this.c_count = c_count;
	}
	public int getC_collect() {
		return c_collect;
	}
	public void setC_collect(int c_collect) {
		this.c_collect = c_collect;
	}
	public float getC_price() {
		return c_price;
	}
	public void setC_price(float c_price) {
		this.c_price = c_price;
	}
	
    
}
